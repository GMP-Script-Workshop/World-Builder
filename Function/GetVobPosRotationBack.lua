	if gVob then
	
		if Button and type (Button) == "number" then
			local x, y, z = gVob:GetRotation();
			
			for i, r in ipairs (RotationBack) do
				if r.Rot == y then
					x, y, z = gVob:GetPosition();
					if Button == 1 then -- A
						return x + r.PosX, y, z + r.PosZ; 
					elseif Button == 2 then -- D
						return x - r.PosX, y, z - r.PosZ; 
					else
						return false;
					end
				end
				
			end
			
		else
			
			return false;
			
		end	
		
	else
	
		return false;
		
	end
	
